//C Program to add two given numbers

#include <stdio.h>
int main()
{
   int num1, num2;

   //Read the numbers from user
   printf("Enter two numbers: ");
   scanf("%d %d", &num1, &num2);

   //Add given two numbers and print
   printf("Sum of given two numbers is = %d\n", num1 + num2);

   return 0;
}
   
   
   